def _sev_cell(sev: Severity) -> str:
    return f'<span class="sev-{sev.value}">{sev.value}</span>'


def build_html(
    summary: ScanSummary,
    records: List[CVERecord],
    llm: Optional[LLMSummary] = None,
) -> str:
    e = html.escape

    def card(n, label: str) -> str:
        return f'<div class="card"><div class="n">{n}</div><div class="l">{label}</div></div>'

    exec_block = ""
    if llm and llm.text:
        engine = "local LLM" if llm.engine in ("ollama", "openai") else "heuristic engine"
        by = f"Generated by {engine}"
        if llm.model and not llm.degraded:
            by += f" ({e(llm.model)})"
        if llm.degraded and llm.note:
            by += f" — fell back: {e(llm.note)}"
        exec_block = (
            '<h2>Executive Summary</h2>'
            f'<div class="exec"><div class="by">{by}</div>{e(llm.text)}</div>'
        )

    rows = []
    for i, item in enumerate(summary.top_findings, 1):
        cvss = f"{item.cvss_score:.1f}" if item.cvss_score is not None else "—"
        epss = f"{item.epss_score * 100:.0f}%" if item.epss_score is not None else "—"
        badges = "".join(
            f'<span class="badge {"kev" if s in ("CISA KEV", "ransomware") else ""}">{e(s)}</span>'
            for s in item.signals
        ) or "—"
        comp = f' <span class="badge">{e(item.component)}</span>' if item.component else ""
        rows.append(
            f"<tr><td>{i}</td>"
            f'<td><a href="{e(item.nvd_link)}">{e(item.cve_id)}</a>{comp}</td>'
            f"<td><b>{item.priority_score:.0f}</b></td>"
            f"<td>{_sev_cell(item.severity)}</td>"
            f"<td>{cvss}</td><td>{epss}</td><td>{badges}</td></tr>"
        )
    findings_table = (
        "<h2>Prioritised Findings</h2><table><tr><th>#</th><th>CVE</th>"
        "<th>Priority</th><th>Severity</th><th>CVSS</th><th>EPSS</th><th>Signals</th></tr>"
        + "".join(rows) + "</table>"
        if rows else ""
    )

    detail = []
    for r in records:
        chips = []
        if r.exploits:
            chips.append(f'<span class="badge">Exploit-DB ×{len(r.exploits)}</span>')
        if r.pocs:
            chips.append(f'<span class="badge">PoC ×{len(r.pocs)}</span>')
        if r.nuclei_templates:
            chips.append('<span class="badge">Nuclei</span>')
        if r.msf_modules:
            chips.append('<span class="badge">Metasploit</span>')
        if r.in_kev:
            chips.append('<span class="badge kev">CISA KEV</span>')
        desc = e((r.description or "").strip()[:600])
        detail.append(
            f'<h3><a href="{e(r.nvd_link)}">{e(r.cve_id)}</a> · {_sev_cell(r.severity)}</h3>'
            f"<div>{desc}</div><div style='margin-top:6px'>{''.join(chips)}</div>"
        )
    detail_block = "<h2>Full CVE Detail</h2>" + "".join(detail) if detail else ""

    cards = "".join([
        card(summary.total_cves, "Total CVEs"),
        card(summary.critical, "Critical"),
        card(summary.in_kev, "In KEV"),
        card(summary.weaponised, "Weaponised"),
        card(summary.with_poc, "With PoC"),
    ])

    return f"""<!doctype html>
<html lang="en"><head><meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>ThreatTracer Report — {e(summary.target or 'scan')}</title>
<style>{_HTML_CSS}</style></head>
<body><div class="wrap">
<h1>ThreatTracer Report</h1>
<div class="sub">{e(summary.target or 'scan')} · {e(_now())} · scan type: <code>{e(summary.scan_type)}</code></div>
{exec_block}
<h2>At a Glance</h2><div class="cards">{cards}</div>
{findings_table}
{detail_block}
<div class="foot">ThreatTracer aggregates public vulnerability intelligence for authorised
testing and defensive triage. Priority scores are decision support, not a guarantee of
exploitability.</div>
</div></body></html>"""


def write_report(
    path: Path,
    summary: ScanSummary,
    records: List[CVERecord],
    llm: Optional[LLMSummary] = None,
) -> Path:
    """Write a report to ``path``; format inferred from the extension (.md/.html)."""
    suffix = path.suffix.lower()
    if suffix in (".html", ".htm"):
        content = build_html(summary, records, llm)
    else:
        content = build_markdown(summary, records, llm)
        if suffix != ".md":
            path = path.with_suffix(".md")
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    return path
