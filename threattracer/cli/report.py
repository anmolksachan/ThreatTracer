"""
threattracer.cli.report
~~~~~~~~~~~~~~~~~~~~~~~~
Self-contained Markdown / HTML report writer.

No heavy dependencies: Markdown is plain text; HTML is a single inlined-CSS file
that renders in any browser and prints cleanly to PDF (Ctrl-P → Save as PDF).
"""

from __future__ import annotations

import html
import logging
from datetime import datetime, timezone
from pathlib import Path
from typing import List, Optional

from threattracer.utils.models import (
    CVERecord,
    LLMSummary,
    ScanSummary,
    Severity,
    TriageItem,
)

log = logging.getLogger(__name__)

_SEV_BADGE = {
    Severity.CRITICAL: "🔴 CRITICAL",
    Severity.HIGH: "🟠 HIGH",
    Severity.MEDIUM: "🟡 MEDIUM",
    Severity.LOW: "🟢 LOW",
    Severity.NONE: "⚪ NONE",
    Severity.UNKNOWN: "❓ UNKNOWN",
}


def _now() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")


# ── Markdown ────────────────────────────────────────────────────────────────

def build_markdown(
    summary: ScanSummary,
    records: List[CVERecord],
    llm: Optional[LLMSummary] = None,
) -> str:
    out: List[str] = []
    out.append(f"# ThreatTracer Report — {summary.target or 'scan'}")
    out.append("")
    out.append(f"*Generated {_now()} · scan type: `{summary.scan_type}`*")
    out.append("")

    if llm and llm.text:
        engine = "local LLM" if llm.engine in ("ollama", "openai") else "heuristic"
        out.append("## Executive Summary")
        out.append("")
        out.append(f"> _Generated by {engine}"
                   + (f" (`{llm.model}`)" if llm.model and not llm.degraded else "")
                   + (f" — {llm.note}" if llm.degraded and llm.note else "")
                   + "_")
        out.append("")
        out.append(llm.text)
        out.append("")

    out.append("## At a Glance")
    out.append("")
    out.append("| Metric | Count |")
    out.append("|---|---|")
    out.append(f"| Total CVEs | {summary.total_cves} |")
    out.append(f"| Critical | {summary.critical} |")
    out.append(f"| High | {summary.high} |")
    out.append(f"| In CISA KEV | {summary.in_kev} |")
    out.append(f"| Ransomware-linked | {summary.ransomware_linked} |")
    out.append(f"| Weaponised (MSF/KEV) | {summary.weaponised} |")
    out.append(f"| With Exploit-DB entry | {summary.with_edb} |")
    out.append(f"| With public PoC | {summary.with_poc} |")
    out.append(f"| With Nuclei template | {summary.with_nuclei} |")
    out.append("")

    if summary.top_findings:
        out.append("## Prioritised Findings")
        out.append("")
        out.append("| # | CVE | Priority | Severity | CVSS | EPSS | Signals |")
        out.append("|---|---|---|---|---|---|---|")
        for i, item in enumerate(summary.top_findings, 1):
            cvss = f"{item.cvss_score:.1f}" if item.cvss_score is not None else "—"
            epss = f"{item.epss_score * 100:.0f}%" if item.epss_score is not None else "—"
            sig = ", ".join(item.signals) or "—"
            comp = f" ({item.component})" if item.component else ""
            out.append(
                f"| {i} | [{item.cve_id}]({item.nvd_link}){comp} | "
                f"{item.priority_score:.0f} | {item.severity.value} | {cvss} | {epss} | {sig} |"
            )
        out.append("")

    if records:
        out.append("## Full CVE Detail")
        out.append("")
        for r in records:
            out.append(f"### {r.cve_id} — {_SEV_BADGE.get(r.severity, r.severity.value)}")
            out.append("")
            if r.description and r.description != "N/A":
                out.append(r.description.strip())
                out.append("")
            meta = []
            if r.cvss_score is not None:
                meta.append(f"**CVSS:** {r.cvss_score}")
            if r.epss_score is not None:
                meta.append(f"**EPSS:** {r.epss_score * 100:.1f}%")
            if r.in_kev:
                meta.append(f"**CISA KEV:** added {r.kev_date_added} "
                            f"(ransomware: {r.kev_ransomware_use or 'unknown'})")
            if meta:
                out.append(" · ".join(meta))
                out.append("")
            if r.exploits:
                out.append(f"- **Exploit-DB ({len(r.exploits)}):** " +
                           ", ".join(f"[EDB-{e.id}]({e.link})" for e in r.exploits[:6]))
            if r.pocs:
                out.append(f"- **PoCs ({len(r.pocs)}):** " +
                           ", ".join(f"[{p.url.split('/')[-1] or p.url}]({p.url})"
                                     for p in r.pocs[:6]))
            if r.nuclei_templates:
                out.append(f"- **Nuclei:** `nuclei -t cves/ -id {r.cve_id.lower()}`")
            if r.msf_modules:
                out.append(f"- **Metasploit:** `use {r.msf_modules[0].fullname}`")
            out.append("")

    out.append("---")
    out.append("")
    out.append("_ThreatTracer aggregates public vulnerability intelligence for "
               "authorised testing and defensive triage. Priority scores are "
               "decision support, not a guarantee of exploitability._")
    out.append("")
    return "\n".join(out)


